public class Array_Char{
	public static void main(String[] args){
			double d[]={1.2,2.1,7.2,1.3,8.3};
			char ch[]={'a','e','i','o','u'};
			for(int i=0; i<5; i++)
				System.out.println("Enter the double number values:"+d[i]);
			for(int j=0; j<5; j++)
				System.out.print(" "+ch[j]);
	}
}